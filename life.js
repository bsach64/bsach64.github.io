function life() {
	let final = JSON.parse(JSON.stringify(state));
	let neighbours = [[-1, -1], [-1, 0], [-1, 1], [1, 0], [1, 1], [0, 1], [0, -1], [1, -1]];	
	for (let i = 0; i < colSize; i++) {
		for (let j = 0; j < rowSize; j++) {
			alive = 0;
			for (let n of neighbours) {
				let ni = i + n[0];
				let nj = j + n[1];
				if (ni >= 0 && ni < colSize && nj >= 0 && nj < rowSize) {
					alive += state[ni][nj];
				}
			}
			if (state[i][j] == 1) {
				if (alive == 2 || alive == 3) {
					final[i][j] = 1;
				} else {
					final[i][j] = 0;
				}
			} else {
				if (alive == 3) {
					final[i][j] = 1;
				}
			}
		}
	}
	state = JSON.parse(JSON.stringify(final));
}

function draw() {
	const gameOfLife = document.getElementById("gol");
	if (!gameOfLife.getContext) {
		return;
	}
	
	const ctx = gameOfLife.getContext("2d");
	ctx.clearRect(0, 0, gameOfLife.width, gameOfLife.height);
	ctx.fillStyle = "rgb(255 255 255)";
	for (let i = 0; i < colSize; i++) {
		for (let j = 0; j < rowSize; j++) {
			if (state[i][j] == 1) {
				ctx.fillRect(1 + (j * 15), 1 + (i * 15), 13, 13);
			}
		}
	}
	life();
}

setInterval(draw, 200);